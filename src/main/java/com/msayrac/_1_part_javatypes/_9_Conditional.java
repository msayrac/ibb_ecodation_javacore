package com.msayrac._1_part_javatypes;

public class _9_Conditional {

    public static void main(String[] args) {

        int number   =0;

        if (number>0){
            System.out.println("Number > 0 +");
        }
        else if (number<0){
            System.out.println("Number < 0 -   ");
        }
        else{
            System.out.println("Number  = 0");
        }
    }
}
